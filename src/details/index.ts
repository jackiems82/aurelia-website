import {RouterConfiguration, Router} from 'aurelia-router';

export class DetailsRouter {
  router: Router;

  configureRouter(config: RouterConfiguration, router: Router): void {
    config.title = 'Details';

    config.map([      
      { route: '1',     title: '1',  name: '1', moduleId: 'board-details', nav:true },
      { route: '2',     title: '2',  name: '2',      moduleId: 'board-details', nav:true },
      { route: '3',   title: '3',   name: '3',    moduleId: 'board-details', nav:true },
      { route: '4',  title: '4',  name: '4',   moduleId: 'board-details', nav:true }
    ]);

    this.router = router;
  }
}